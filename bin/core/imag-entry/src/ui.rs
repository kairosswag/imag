use clap::{Arg, ArgGroup, App, SubCommand};

pub fn build_ui<'a>(app: App<'a, 'a>) -> App<'a, 'a> {
    app
        .arg(Arg::with_name("entry")
             .index(1)
             .takes_value(true)
             .required(false)
             .multiple(true)
             .help("The entry/entries to edit")
             .value_name("ENTRY"))
        .arg(Arg::with_name("entries-from-stdin")
             .long("ids-from-stdin")
             .short("I")
             .takes_value(false)
             .required(false)
             .multiple(false)
             .help("The entry/entries are piped in via stdin"))
        .group(ArgGroup::with_name("input-method")
               .args(&["entry", "entries-from-stdin"])
               .required(true))

        .arg(Arg::with_name("list-id")
             .long("list-id")
             .short("l")
             .takes_value(false)
             .required(false)
             .multiple(false)
             .help("List Store Id in output (format: '<id> - <value>'"))
        .arg(Arg::with_name("list-id-format")
             .long("list-id-format")
             .short("L")
             .takes_value(true)
             .required(false)
             .multiple(false)
             .help("List Store Id in output with format"))

        .subcommand(SubCommand::with_name("header")
                    .about("Access data from the header")
                    .version("0.1")

                    .subcommand(SubCommand::with_name("read")
                                .about("Read a header value by path")
                                .version("0.1")
                                .arg(Arg::with_name("header-value-path")
                                     .index(1)
                                     .takes_value(true)
                                     .required(true)
                                     .multiple(false)
                                     .help("Path of the header value")
                                     .value_name("PATH"))
                               )

                    .subcommand(SubCommand::with_name("has")
                                .about("Check whether a header value is present")
                                .version("0.1")
                                .arg(Arg::with_name("header-value-path")
                                     .index(1)
                                     .takes_value(true)
                                     .required(true)
                                     .multiple(false)
                                     .help("Path of the header value")
                                     .value_name("PATH"))
                               )

                    .subcommand(SubCommand::with_name("hasnt")
                                .about("Check whether a header value is not present")
                                .version("0.1")
                                .arg(Arg::with_name("header-value-path")
                                     .index(1)
                                     .takes_value(true)
                                     .required(true)
                                     .multiple(false)
                                     .help("Path of the header value")
                                     .value_name("PATH"))
                               )

                    .subcommand(SubCommand::with_name("int")
                                .about("Check whether a header value is a number")
                                .version("0.1")
                                .arg(Arg::with_name("header-value-path")
                                     .index(1)
                                     .takes_value(true)
                                     .required(true)
                                     .multiple(false)
                                     .help("Path of the header value")
                                     .value_name("PATH"))

                                .arg(Arg::with_name("header-int-eq")
                                     .short("eq")
                                     .long("equal")
                                     .takes_value(true)
                                     .required(false)
                                     .help("Check whether the value is equal to VALUE")
                                     .validator(::libimagutil::cli_validators::is_integer)
                                     .value_name("VALUE"))
                                .arg(Arg::with_name("header-int-neq")
                                     .short("neq")
                                     .long("notequal")
                                     .takes_value(true)
                                     .required(false)
                                     .help("Check whether the value is not equal to VALUE")
                                     .validator(::libimagutil::cli_validators::is_integer)
                                     .value_name("VALUE"))
                                .arg(Arg::with_name("header-int-lt")
                                     .short("lt")
                                     .long("lowerthan")
                                     .takes_value(true)
                                     .required(false)
                                     .help("Check whether the value is lower than VALUE")
                                     .validator(::libimagutil::cli_validators::is_integer)
                                     .value_name("VALUE"))
                                .arg(Arg::with_name("header-int-gt")
                                     .short("gt")
                                     .long("greatherthan")
                                     .takes_value(true)
                                     .required(false)
                                     .help("Check whether the value is greater than VALUE")
                                     .validator(::libimagutil::cli_validators::is_integer)
                                     .value_name("VALUE"))
                                .arg(Arg::with_name("header-int-lte")
                                     .short("lte")
                                     .long("lowerthanequal")
                                     .takes_value(true)
                                     .required(false)
                                     .help("Check whether the value is lower than or equal VALUE")
                                     .validator(::libimagutil::cli_validators::is_integer)
                                     .value_name("VALUE"))
                                .arg(Arg::with_name("header-int-gte")
                                     .short("gte")
                                     .long("greatherthanequal")
                                     .takes_value(true)
                                     .required(false)
                                     .help("Check whether the value is greater than or equal VALUE")
                                     .validator(::libimagutil::cli_validators::is_integer)
                                     .value_name("VALUE"))
                               )

                    .subcommand(SubCommand::with_name("float")
                                .about("Check whether a header value is a floating number")
                                .version("0.1")
                                .arg(Arg::with_name("header-value-path")
                                     .index(1)
                                     .takes_value(true)
                                     .required(true)
                                     .multiple(false)
                                     .help("Path of the header value")
                                     .value_name("PATH"))

                                .arg(Arg::with_name("header-float-eq")
                                     .short("eq")
                                     .long("equal")
                                     .takes_value(true)
                                     .required(false)
                                     .help("Check whether the value is equal to VALUE")
                                     .validator(::libimagutil::cli_validators::is_float)
                                     .value_name("VALUE"))
                                .arg(Arg::with_name("header-float-neq")
                                     .short("neq")
                                     .long("notequal")
                                     .takes_value(true)
                                     .required(false)
                                     .help("Check whether the value is not equal to VALUE")
                                     .validator(::libimagutil::cli_validators::is_float)
                                     .value_name("VALUE"))
                                .arg(Arg::with_name("header-float-lt")
                                     .short("lt")
                                     .long("lowerthan")
                                     .takes_value(true)
                                     .required(false)
                                     .help("Check whether the value is lower than VALUE")
                                     .validator(::libimagutil::cli_validators::is_float)
                                     .value_name("VALUE"))
                                .arg(Arg::with_name("header-float-gt")
                                     .short("gt")
                                     .long("greatherthan")
                                     .takes_value(true)
                                     .required(false)
                                     .help("Check whether the value is greater than VALUE")
                                     .validator(::libimagutil::cli_validators::is_float)
                                     .value_name("VALUE"))
                               )

                    .subcommand(SubCommand::with_name("string")
                                .about("Check whether a header value is a string")
                                .version("0.1")
                                .arg(Arg::with_name("header-value-path")
                                     .index(1)
                                     .takes_value(true)
                                     .required(true)
                                     .multiple(false)
                                     .help("Path of the header value")
                                     .value_name("PATH"))

                                .arg(Arg::with_name("header-string-eq")
                                     .short("eq")
                                     .long("equal")
                                     .takes_value(true)
                                     .required(false)
                                     .help("Check whether the value is equal to VALUE")
                                     .value_name("VALUE"))
                                .arg(Arg::with_name("header-string-neq")
                                     .short("neq")
                                     .long("notequal")
                                     .takes_value(true)
                                     .required(false)
                                     .help("Check whether the value is not equal to VALUE")
                                     .value_name("VALUE"))
                               )

                    .subcommand(SubCommand::with_name("bool")
                                .about("Check whether a header value is a bool")
                                .version("0.1")
                                .arg(Arg::with_name("header-value-path")
                                     .index(1)
                                     .takes_value(true)
                                     .required(true)
                                     .multiple(false)
                                     .help("Path of the header value")
                                     .value_name("PATH"))

                                .arg(Arg::with_name("header-bool-set")
                                     .short("t")
                                     .long("set")
                                     .takes_value(false)
                                     .required(false)
                                     .validator(::libimagutil::cli_validators::is_bool)
                                     .help("Check whether the flag is set (true)"))
                                .arg(Arg::with_name("header-bool-unset")
                                     .short("f")
                                     .long("unset")
                                     .takes_value(false)
                                     .required(false)
                                     .validator(::libimagutil::cli_validators::is_bool)
                                     .help("Check whether the flag is unset (false)"))
                               )
                    )

        .subcommand(SubCommand::with_name("content")
                    .about("Access the content part of the entries")
                    .version("0.1")
                    // To be defined
                    )

        .subcommand(SubCommand::with_name("exec")
                    .about("Execute a script on the entry (script has only READ access!)")
                    .version("0.1")
                    .arg(Arg::with_name("script")
                         .index(1)
                         .takes_value(true)
                         .required(false)
                         .multiple(false)
                         .help("Script to execute")
                         .value_name("SCRIPT"))
                    .arg(Arg::with_name("scriptfile")
                         .index(1)
                         .takes_value(true)
                         .required(false)
                         .multiple(false)
                         .help("File that contains the script")
                         .value_name("PATH"))
                    .group(ArgGroup::with_name("script-text")
                           .args(&["script", "scriptfile"])
                           .required(true))

                    .arg(Arg::with_name("scriptlang-lua")
                         .long("lua")
                         .takes_value(false)
                         .required(false)
                         .multiple(false)
                         .help("Hint that the script is a LUA script"))

                    .arg(Arg::with_name("scriptlang-ketos")
                         .long("ketos")
                         .takes_value(false)
                         .required(false)
                         .multiple(false)
                         .help("Hint that the script is a KETOS script"))

                    .arg(Arg::with_name("scriptlang-dyon")
                         .long("dyon")
                         .takes_value(false)
                         .required(false)
                         .multiple(false)
                         .help("Hint that the script is a DYON script"))
                   )

}
